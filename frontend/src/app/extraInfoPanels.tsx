"use client";

import { useState, useContext } from "react";
import { Formik, Form, Field } from "formik";
import "./globals.css";
import { Action, Character } from "./types";
import { Button } from "../components/ui/button";
import { saveActionToIndexedDB } from "../lib/indexedDB";
import { Divider } from "@/components/ui/Divider";
import { AppContext } from "./context";
import InputSmartNumber from "@/components/ui/inputSmartNumber";

export default function ExtraInfoPanels() {
   const context = useContext(AppContext);
   if (!context) {
      throw new Error("AppContext must be used within a Provider");
   }

   const { setActions, character, setCharacter } = context;
   const [activeTab, setActiveTab] = useState<"addAction" | "attributes">(
      "addAction"
   );

   const handleSubmit = async (values: Action, { resetForm }: any) => {
      try {
         const id = await saveActionToIndexedDB(values);
         setActions((prev) => [...prev, values]);
         resetForm();
      } catch (error) {
         console.error("Error saving action:", error);
      }
   };

   const handleAttributeChange = (key: keyof Character, value: number) => {
      if (!character) return;
      const updatedCharacter = { ...character, [key]: value };
      setCharacter(updatedCharacter);
   };

   const calculateModifier = (attribute: number): string => {
      const modifier = Math.floor((attribute - 10) / 2);
      if (modifier >= 0) {
         return `+${modifier}`;
      }
      return `${modifier}`;
   };

   type FormValues = Omit<Action, "triggers" | "id" | "characterID"> & {
      triggers: string;
   };

   return (
      <div className="flex flex-1 flex-col p-8">
         {/* Tabs */}
         <div className="flex gap-4 mb-4">
            <button
               className={`p-2 rounded ${
                  activeTab === "addAction"
                     ? "bg-contrast-2 text-white"
                     : "bg-contrast-3 text-gray-400"
               }`}
               onClick={() => setActiveTab("addAction")}
            >
               Add Action
            </button>
            <button
               className={`p-2 rounded ${
                  activeTab === "attributes"
                     ? "bg-contrast-2 text-white"
                     : "bg-contrast-3 text-gray-400"
               }`}
               onClick={() => setActiveTab("attributes")}
            >
               Attributes
            </button>
         </div>

         {/* Tab Content */}
         {activeTab === "addAction" ? (
            <div className="flex-1 flex bg-contrast-1 justify-center items-center">
               <Formik<FormValues>
                  initialValues={{
                     title: "",
                     description: "",
                     time: undefined,
                     attack: undefined,
                     triggers: "",
                  }}
                  onSubmit={(values, actions) => {
                     if (!character) {
                        console.error("Character not found");
                        return;
                     }

                     const formattedValues: Action = {
                        ...values,
                        id: 0, // ID will be generated by IndexedDB
                        characterID: character.id,
                        triggers: values.triggers
                           .split(",")
                           .map((t) => t.trim()),
                     };
                     handleSubmit(formattedValues, actions);
                  }}
               >
                  {() => (
                     <Form className="bg-contrast-2 p-6 rounded shadow-md w-full max-w-md">
                        <Divider />
                        <h2 className="text-xl font-bold mb-4">
                           Add New Action
                        </h2>
                        <div className="mb-4">
                           <label className="block mb-1" htmlFor="title">
                              Title
                           </label>
                           <Field
                              id="title"
                              name="title"
                              placeholder="Action Title"
                              className="w-full p-2 rounded bg-contrast-3 text-white"
                           />
                        </div>
                        <div className="mb-4">
                           <label className="block mb-1" htmlFor="description">
                              Description
                           </label>
                           <Field
                              id="description"
                              name="description"
                              as="textarea"
                              placeholder="Action Description"
                              className="w-full p-2 rounded bg-contrast-3 text-white"
                           />
                        </div>
                        <div className="mb-4">
                           <label className="block mb-1" htmlFor="time">
                              Time
                           </label>
                           <Field
                              id="time"
                              name="time"
                              placeholder="e.g., Action, Bonus Action"
                              className="w-full p-2 rounded bg-contrast-3 text-white"
                           />
                        </div>
                        <div className="mb-4">
                           <label className="block mb-1" htmlFor="attack">
                              Attack
                           </label>
                           <Field
                              id="attack"
                              name="attack"
                              placeholder="e.g., 1d8 slashing"
                              className="w-full p-2 rounded bg-contrast-3 text-white"
                           />
                        </div>
                        <div className="mb-4">
                           <label className="block mb-1" htmlFor="triggers">
                              Triggers (comma-separated)
                           </label>
                           <Field
                              id="triggers"
                              name="triggers"
                              placeholder="e.g., Enemy casts spell, Bonus Action"
                              className="w-full p-2 rounded bg-contrast-3 text-white"
                           />
                        </div>
                        <Button type="submit" className="w-full">
                           Add Action
                        </Button>
                     </Form>
                  )}
               </Formik>
            </div>
         ) : (
            <div className="grid grid-cols-6 gap-4">
               {[
                  { key: "str", title: "Strength" },
                  { key: "dex", title: "Dexterity" },
                  { key: "con", title: "Constitution" },
                  { key: "int", title: "Intelligence" },
                  { key: "wis", title: "Wisdom" },
                  { key: "cha", title: "Charisma" },
               ].map(({ key, title }) => (
                  <div key={key} className="flex flex-col items-center">
                     <div className="text-sm font-bold mb-1">{title}</div>
                     <div className="bg-contrast-2 text-white w-16 h-16 flex items-center justify-center rounded">
                        {calculateModifier(
                           character?.[key as keyof Character] || 10
                        )}
                     </div>
                     <InputSmartNumber
                        value={character?.[key as keyof Character] || 10}
                        onChange={(value) =>
                           handleAttributeChange(
                              key as keyof Character,
                              Number(value.target.value)
                           )
                        }
                        // className="bg-contrast-3 text-white w-100 text-center rounded mt-2"
                        className="w-10 text-contrast-10 bg-contrast-3 opacity-100 rounded mt-[-10px]"
                     />
                  </div>
               ))}
            </div>
         )}
      </div>
   );
}
